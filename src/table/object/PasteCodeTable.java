package table.object;

public class PasteCodeTable {
    private int count;
    private String className;
    private int ndi;
    private int nidi;
    private int ti;
    private int ci;

    public PasteCodeTable(int count, String className, int ndi, int nidi, int ti, int ci) {
        this.count = count;
        this.className = className;
        this.ndi = ndi;
        this.nidi = nidi;
        this.ti = ti;
        this.ci = ci;
    }
}
